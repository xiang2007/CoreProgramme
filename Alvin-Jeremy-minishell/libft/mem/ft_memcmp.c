#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int				diff;
	unsigned char	*ptr1;
	unsigned char	*ptr2;

	ptr1 = (unsigned char *)s1;
	ptr2 = (unsigned char *)s2;
	while (n--)
	{
		diff = *ptr1 - *ptr2;
		if (diff)
			return (diff);
		ptr1++;
		ptr2++;
	}
	return (0);
}
