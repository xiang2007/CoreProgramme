#include "libft.h"

char	*ft_itoa(int n)
{
	int		i;
	char	temp[12];

	i = 11;
	temp[i] = '\0';
	while (n / 10)
	{
		if (n < 0)
			temp[--i] = '0' - (n % 10);
		else
			temp[--i] = '0' + (n % 10);
		n /= 10;
	}
	if (n < 0)
	{
		temp[--i] = '0' - (n % 10);
		temp[--i] = '-';
	}
	else
		temp[--i] = '0' + (n % 10);
	return (ft_strdup(temp + i));
}
